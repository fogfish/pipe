%% @doc
%%   
-module(pipe_echo).
-behaviour(pipe).

-export([
   start_link/0
  ,init/1
  ,free/2
  ,handle/3
]).

%%%----------------------------------------------------------------------------   
%%%
%%% Factory
%%%
%%%----------------------------------------------------------------------------   

start_link() ->
   pipe:start_link(?MODULE, [], []).

init(_) ->
   {ok, handle, undefined}.

free(_, _) ->
   ok.

%%%----------------------------------------------------------------------------   
%%%
%%% pipe
%%%
%%%----------------------------------------------------------------------------   

handle(Msg, Pipe, State) ->
   pipe:ack(Pipe, Msg),
   {next_state, handle, State}.

